package sweety;

public class Pattern4_number {

	public static void main(String[] args) {
		int i, j,k=1;   	
		for(i=1; i<=4; i++)  
		{   
		
		for(j=1; j<i+1; j++)  
		{   	
		System.out.print(k++ +" ");   
		//incrementing the value of number   
		   
		}   	
		System.out.println();   
		}

	}

}
